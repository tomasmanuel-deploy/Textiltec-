export function t(key: string, lang: Language, vars?: Record<string, string | number>): string {
  let base = dicts[lang]?.[key] ?? dicts['pt']?.[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      base = base.replace(new RegExp(`{${k}}`, 'g'), String(v));
    }
  }
  return base;
}
